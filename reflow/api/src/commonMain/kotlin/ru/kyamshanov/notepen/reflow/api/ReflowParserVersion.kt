package ru.kyamshanov.notepen.reflow.api

/**
 * Версия парсера/ассемблера reflow-документов.
 *
 * Бампается при ЛЮБОМ изменении эвристик извлечения ([ReflowDocument]-сборка:
 * `ReflowAssembler`, табличные детекторы, `TableNoiseGuard`, heading-промоушн,
 * dehyphenation, XY-cut и т.п.), чтобы дисковый кэш разобранного документа
 * инвалидировался и книга переразбиралась НОВЫМ кодом.
 *
 * Зачем: кэш валиден по `(sourceSize, sourceMtime)` исходного файла. У статичной
 * книги они не меняются никогда, поэтому без отдельного версионирования парсера
 * документ, открытый до обновления приложения, НАВСЕГДА показывал бы старый
 * разбор — например фантомные таблицы из OCR-прозы, которые чинит F-1/F-8
 * (`TableNoiseGuard`). Реальный пользователь не может вручную чистить кэш, как
 * это делалось в QA-прогоне; инвалидация по версии парсера закрывает разрыв (F-9).
 *
 * История:
 *  - `1`: первая версия с инвалидацией по версии парсера (F-9). Покрывает
 *    `TableNoiseGuard` (F-1/F-7/F-8), `TableRow.isHeader` (F-2), эвристики
 *    wave 3 / wave 4.
 *
 * Сериализуется в заголовок дискового кэша; при несовпадении запись считается
 * устаревшей и переразбирается.
 */
public const val REFLOW_PARSER_VERSION: Int = 1
