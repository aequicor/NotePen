package ru.kyamshanov.notepen.reflow.api

/**
 * Тип содержимого PDF с точки зрения применимости reflow (переформатирования
 * текста под произвольную ширину колонки и типографику).
 *
 * Классифицируется по наличию извлекаемого текстового слоя — см.
 * [PdfReflowExtractor.probe].
 */
public enum class PdfContentKind {
    /**
     * На странице есть извлекаемый текстовый слой с координатами глифов —
     * reflow применим напрямую.
     */
    TEXT_BASED,

    /**
     * Текстового слоя нет: страницы — растровые изображения (скан).
     * Для reflow требуется предварительный OCR.
     */
    IMAGE_ONLY,

    /**
     * Часть страниц содержит текст, часть — нет (типично для отсканированных
     * документов с частичным OCR-слоем или со вставками-картинками).
     */
    HYBRID,
}
