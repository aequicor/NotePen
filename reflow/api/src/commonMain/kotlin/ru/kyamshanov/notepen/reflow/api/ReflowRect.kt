package ru.kyamshanov.notepen.reflow.api

/**
 * Прямоугольная область, нормализованная к размеру PDF-страницы: координаты в
 * долях `[0..1]`, где 0 — левый/верхний край страницы, а 1 — правый/нижний.
 * Начало координат — верхний левый угол, ось Y направлена вниз. Это та же
 * система координат, что и у точек штрихов-аннотаций, поэтому область и штрих
 * можно сравнивать напрямую без пересчёта. Значения могут слегка выходить за
 * `[0..1]` у самого края страницы.
 *
 * @property left левая граница (доля ширины страницы)
 * @property top верхняя граница (доля высоты страницы)
 * @property right правая граница (доля ширины страницы)
 * @property bottom нижняя граница (доля высоты страницы)
 */
public data class ReflowRect(
    public val left: Float,
    public val top: Float,
    public val right: Float,
    public val bottom: Float,
) {
    /** Ширина области в долях ширины страницы. */
    public val width: Float get() = right - left

    /** Высота области в долях высоты страницы. */
    public val height: Float get() = bottom - top
}
