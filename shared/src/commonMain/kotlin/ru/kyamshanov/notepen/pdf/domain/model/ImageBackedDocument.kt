package ru.kyamshanov.notepen.pdf.domain.model

/**
 * Документ, источником которого является растровое изображение (PNG/JPEG), а не PDF.
 *
 * Представляется как [PdfDocument] из ровно одной страницы, чтобы весь конвейер
 * отрисовки и рисования (потребляющий только [PdfPageInfo] + ARGB-буфер
 * [PdfPageData]) работал без изменений. Несёт уже декодированные пиксели, чтобы
 * рендерер мог масштабировать их под целевой размер без повторного декодирования.
 *
 * Маркерный тип позволяет композитному рендереру отличать изображение от PDF,
 * не завязываясь на платформенные реализации.
 */
interface ImageBackedDocument : PdfDocument {
    /** Ширина исходного изображения в пикселях. */
    val widthPx: Int

    /** Высота исходного изображения в пикселях. */
    val heightPx: Int

    /**
     * Пиксели изображения в формате ARGB (тот же layout, что у [PdfPageData.pixels]):
     * строка за строкой, слева направо, сверху вниз. Размер массива = [widthPx] × [heightPx].
     */
    val pixels: IntArray
}
