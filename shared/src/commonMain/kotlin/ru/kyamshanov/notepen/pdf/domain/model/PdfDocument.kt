package ru.kyamshanov.notepen.pdf.domain.model

/**
 * Дескриптор открытого PDF-документа.
 *
 * Реализации не обязаны быть thread-safe: все обращения к одному экземпляру
 * должны происходить из одного корутинного контекста.
 * После вызова [close] поведение любых методов не определено.
 */
interface PdfDocument {

    /** Метаданные документа: число страниц и их геометрия. */
    val info: PdfDocumentInfo

    /** Освобождает все ресурсы, связанные с документом. Идемпотентен. */
    fun close()
}
