package ru.kyamshanov.notepen.mainscreen.domain.model

/** Статус доступности файла в истории недавних файлов. */
enum class AvailabilityStatus {
    /** Статус ещё не проверен (начальное состояние после загрузки истории). */
    UNKNOWN,

    /** Файл доступен и открываем. */
    AVAILABLE,

    /** Файл не найден на диске (удалён или перемещён). */
    NOT_FOUND,

    /** Файл найден, но повреждён, зашифрован или недоступен по иной причине. */
    FILE_ERROR,

    /**
     * Файл был вытеснен из истории (eviction LRU) и при попытке открытия
     * из папки оказался недоступен.
     */
    ARCHIVED_UNAVAILABLE,
}
