package ru.kyamshanov.notepen.annotation.domain.model

import kotlinx.serialization.Serializable

/**
 * Выделение «липкого маркера»: набор слово-выровненных областей поверх текстового
 * слоя PDF, к которым «прилипло» выделение.
 *
 * **Геометрия — источник истины.** В отличие от диапазона символов, набор
 * прямоугольников не «уезжает» при переэкстракции текста между версиями приложения,
 * поэтому в редакторе он рисуется напрямую. Диапазон символов для подсветки в режиме
 * чтения вычисляется из [rects] на лету, а не хранится.
 *
 * Прямоугольники нормализованы к странице, под индексом которой это выделение лежит
 * в карте (ключ карты несёт номер страницы — как и у штрихов), и обычно содержат по
 * одному прямоугольнику на строку текста.
 *
 * @property rects слово-выровненные нормализованные области (см. [NormalizedRect])
 * @property colorArgb упакованный ARGB цвета маркера; альфа пресета уже включена
 * @property strokeId стабильный идентификатор для синхронизации; пустая строка — локальное
 */
@Serializable
data class StickyHighlight(
    val rects: List<NormalizedRect> = emptyList(),
    val colorArgb: Long = MarkerSettings.PRESET_COLORS[0],
    val strokeId: String = "",
)
