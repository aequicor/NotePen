package ru.kyamshanov.notepen.annotation.domain.model

/**
 * Лёгкое состояние вида документа (масштаб и позиция прокрутки), хранимое
 * отдельно от тяжёлого набора штрихов.
 *
 * Выделено в самостоятельную сущность, чтобы при открытии документа можно было
 * восстановить зум/страницу ДО парсинга всех штрихов — иначе страница сначала
 * рисовалась на дефолтном масштабе и резко «доворачивалась» к сохранённому
 * через 1–2 с (после загрузки всего файла аннотаций).
 *
 * Позиция reflow-режима хранится **в индексах блоков** (а не страницах ридера) —
 * номер страницы нестабилен при ре-пагинации (смена шрифта/полей/ориентации),
 * а индекс блока — это «валюта» TextAnchor в reflow-документе. Хранится как
 * плоские Int, чтобы не тащить `:reflow:api` в зависимости `:drawing:api`.
 *
 * @property scale масштаб в процентах.
 * @property currentPage индекс верхней видимой страницы.
 * @property currentPageOffset вертикальный сдвиг внутри [currentPage] в пикселях.
 * @property readingMode включён ли режим чтения (reflow) для документа.
 * @property reflowAnchorBlockIndex индекс блока, открывающего текущую страницу
 *   в reflow-режиме (используется как `TextAnchor.blockIndex`). По умолчанию 0
 *   — начало документа, что совпадает с поведением на свежем открытии.
 * @property reflowAnchorCharStart смещение внутри блока для Phase B precision
 *   (line→char через TextLayoutResult). В Phase A всегда 0; поле здесь, чтобы
 *   Phase B не требовала миграции диск-формата.
 * @property pageRotations пользовательский поворот страницы (поверх собственного
 *   поворота PDF), в четвертях оборота `[0, 3]` по часовой стрелке, по индексу
 *   ЛОГИЧЕСКОЙ страницы. Отсутствие ключа эквивалентно `0` (без доворота).
 *   Хранится в лёгком сайдкаре вида, чтобы layout/растеризация применяли поворот
 *   ещё до парсинга всех штрихов. Штрихи на диске уже лежат в повёрнутой системе
 *   координат, поэтому при загрузке не нужно их пере-трансформировать — поворот
 *   применяется только к растру и раскладке. При включённом [spreadSplit] ключ —
 *   индекс логической половины (`2S`/`2S+1`), см. [SpreadSplit].
 * @property spreadSplit включено ли ручное разделение разворотов (FEATURE #4):
 *   каждая исходная страница делится на левую/правую логические половины,
 *   удваивая число страниц. Персистится в сайдкаре вида, чтобы загрузиться ДО
 *   штрихов и сразу задать раскладку/индекс-пространство. На диске штрихи и
 *   повороты при включённом разделении лежат уже в ЛОГИЧЕСКОМ (половинном)
 *   пространстве, поэтому при загрузке трансформировать их не нужно.
 * @property spreadViewOverride ЯВНЫЙ выбор пользователя по книжному развороту
 *   (FEATURE #5, две страницы рядом): `null` — пользователь не переключал, режим
 *   авто (включается на широких экранах); `true`/`false` — принудительно вкл/выкл
 *   независимо от ширины экрана. Отделён от [spreadSplit] (FEATURE #4) и
 *   [readingMode] (reflow) — это три разных независимых переключателя. Штрихи на
 *   диске НЕ зависят от разворота (он чисто визуальный: пейринг логических
 *   страниц по X), поэтому миграции координат тут нет.
 */
data class AnnotationViewState(
    val scale: Int = 100,
    val currentPage: Int = 0,
    val currentPageOffset: Int = 0,
    val readingMode: Boolean = false,
    val reflowAnchorBlockIndex: Int = 0,
    val reflowAnchorCharStart: Int = 0,
    val pageRotations: Map<Int, Int> = emptyMap(),
    val spreadSplit: Boolean = false,
    val spreadViewOverride: Boolean? = null,
)
