package ru.kyamshanov.notepen.book

/**
 * Порт конвертации электронной книги (EPUB, FB2, …) в PDF при открытии. Весь
 * остальной конвейер приложения (постраничный просмотр, рукописные пометки,
 * экспорт, reflow-чтение) работает с PDF, поэтому книга единожды верстается в
 * PDF, а дальше используется как обычный документ.
 *
 * Реализации обязаны быть main-safe: блокирующее IO/CPU выполняется через
 * инжектируемый [kotlinx.coroutines.CoroutineDispatcher], а не через
 * `Dispatchers.*` напрямую.
 */
interface EbookToPdfConverter {
    /**
     * Быстрая проверка (без чтения всего файла), поддерживается ли формат
     * источника (см. [detectBookFormat]). Платформенная реализация может
     * опираться на MIME (Android `content://`) с откатом на расширение пути.
     *
     * @param path абсолютный путь к файлу или URI
     * @return `true`, если источник следует открывать как книгу через конвертацию
     */
    fun canConvert(path: String): Boolean

    /**
     * Гарантирует наличие PDF-версии книги и возвращает путь к ней. Результат
     * кешируется: повторный вызов для неизменного источника не выполняет
     * конвертацию заново.
     *
     * @param path абсолютный путь к файлу или URI книги
     * @return абсолютный путь к сконвертированному PDF в кеше приложения
     * @throws IllegalArgumentException если файл не найден или не является поддерживаемой книгой
     */
    suspend fun ensurePdf(path: String): String
}
