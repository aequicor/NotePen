package ru.kyamshanov.notepen

import androidx.compose.runtime.Composable

/**
 * Перехватывает аппаратные клавиши громкости и направляет их в перелистывание
 * ридера, пока [enabled] (т.е. активен режим чтения и панель в фокусе).
 *
 * Нужен отдельно от общего Compose `onKeyEvent`, потому что `Key.VolumeUp/Down`
 * доходят до Compose только при наличии фокуса у focusable, а некоторые прошивки
 * к тому же показывают системный регулятор громкости, если событие не съели
 * раньше — на уровне окна Activity. Реализация перехватывает их там и съедает,
 * чтобы во время чтения громкость листала, а не меняла звук.
 *
 * Контракт: пока [enabled] == `true`, события `VOLUME_DOWN` вызывают
 * `onPageDelta(+1)` (следующая страница), `VOLUME_UP` — `onPageDelta(-1)`
 * (предыдущая), и оба съедаются. Как только [enabled] становится `false` либо
 * композабл покидает композицию — перехват снимается, и громкость работает штатно.
 *
 * На десктопе клавиш громкости нет — actual там no-op (стрелки/PageUp-Down/Space
 * уже обрабатываются общим `onKeyEvent`).
 *
 * @param enabled включить перехват громкости (только в режиме чтения)
 * @param onPageDelta листнуть на ±N страниц (+1 — следующая, −1 — предыдущая)
 */
@Composable
expect fun ReaderVolumeKeyHandler(
    enabled: Boolean,
    onPageDelta: (Int) -> Unit,
)
