package ru.kyamshanov.notepen.epub

/**
 * Порт конвертации EPUB в PDF при открытии. Весь остальной конвейер приложения
 * (постраничный просмотр, рукописные пометки, экспорт, reflow-чтение) работает с
 * PDF, поэтому EPUB единожды верстается в PDF, а дальше используется как обычный
 * документ.
 *
 * Реализации обязаны быть main-safe: блокирующее IO/CPU выполняется через
 * инжектируемый [kotlinx.coroutines.CoroutineDispatcher], а не через
 * `Dispatchers.*` напрямую.
 */
interface EpubToPdfConverter {

    /**
     * Быстрая проверка (без чтения всего файла), является ли источник EPUB.
     * Платформенная реализация может опираться на MIME (Android `content://`)
     * с откатом на расширение пути.
     *
     * @param path абсолютный путь к файлу или URI
     * @return `true`, если источник следует открывать как EPUB
     */
    fun isEpub(path: String): Boolean

    /**
     * Гарантирует наличие PDF-версии EPUB и возвращает путь к ней. Результат
     * кешируется: повторный вызов для неизменного источника не выполняет
     * конвертацию заново.
     *
     * @param path абсолютный путь к файлу или URI EPUB
     * @return абсолютный путь к сконвертированному PDF в кеше приложения
     * @throws IllegalArgumentException если файл не найден или не является валидным EPUB
     */
    suspend fun ensurePdf(path: String): String
}
