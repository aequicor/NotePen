package ru.kyamshanov.notepen.book

import ru.kyamshanov.notepen.reflow.api.ReflowDocument

/**
 * Поставщик reflow-документа для конвертированной книги (EPUB/FB2). Документ
 * собирается рендерером при верстке книги в PDF и сохраняется сайдкаром рядом с
 * кешем; этот порт отдаёт его для режима чтения, минуя обратное извлечение
 * текста из PDF.
 *
 * Реализуется конвертером книг (он знает соответствие путь → кеш PDF). Для не-книг
 * и при отсутствии сайдкара (например, старый кеш) возвращает `null` — вызывающая
 * сторона откатывается на извлечение из PDF.
 */
interface BookReflowProvider {
    /**
     * @param path путь/URI открытого документа (тот же, что при открытии)
     * @return reflow-документ книги или `null`, если источник не книга либо
     *   сайдкар недоступен
     */
    suspend fun reflowFor(path: String): ReflowDocument?
}
